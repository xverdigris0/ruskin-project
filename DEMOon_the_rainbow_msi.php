         <div id="TITLE" class="section">
            <span class="head-section">Title</span>
            
            <p><span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span></p>
            
            <p><span class="persName-JR">Ruskin</span> wrote the title as <span class="title-poem-ONTHERAINBOW">&#x201C;on the rainbow / in blank verse&#x201D;</span>. 
                        He also designated the poem as &#x201C;poem VI&#x201D; in
                        <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>.
            </p>
            
            <p>See also <a href="../notes/title_citation_note.php" target="_blank">System of Title Citation for Works</a>.
            </p>
            
         </div>
         
         <div id="GENRE" class="section">
            <span class="head-section">Genre</span>
            
            <p>Poem. <span class="persName-JR">Ruskin</span> applied the term <i>blank verse</i> also to 
                        <a href="../apparatuses/time_blank_verse_apparatus.php" target="_blank"><span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span></a>
                        and to <a href="../apparatuses/spring_blank_verse_apparatus.php" target="_blank"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a>.
            </p>
            
         </div>
         
         
         <div id="MAJORMS" class="section">
            <span class="head-section">Manuscripts</span>
            
            <p><a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a> (pp. 104&#x2013;6), 
                        a <a href="../notes/red_book_note.php" target="_blank">Red Book</a>
                        devoted primarily devoted to <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL1">&#x201C;Harry and Lucy&#x201D; Vol. 1</span></a>. 
                        <span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span> is the sixth poem in 
                        <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>.
            </p>
            
            <p>Facsimile and transcript by permission of Beinecke Rare Book and Manuscript Library, Yale University.</p>
            
         </div>
         
         <div id="DATE" class="section">
            <span class="head-section">Date</span>
            
            <p><span class="date-1827-01-1827-03">January&#x2013;March</span>, perhaps specifically <span class="date-1827-03-21">March 21</span>. 
                        There is a possibility that the poem originated in <span class="date-1826-01-1826-04">early 1826</span>.
            </p>
            
            <p>See <a href="../apparatuses/on_papas_leaving_home_apparatus.php#DATE" target="_blank">&#x201C;On Papa&#x02BC;s Leaving Home&#x201D;: Date</a>. If that poem can be
                        attributed to <span class="date-1827-01-1827-02">January&#x2013;February 1827</span>, but with an outside possibility of its belonging (like potentially all the poems in 
                        <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a>) 
                        to sometime in <span class="date-1826">1826</span>, <span class="date-1827-01-1827-03">January&#x2013;March 1827</span> range bears an even stronger likelihood for 
                        <span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span>, since this poem shares the subtitle &#x201C;Blank Verse&#x201D; with
                        both <a href="../apparatuses/time_blank_verse_apparatus.php" target="_blank"><span class="title-poem-TIMEBLANKVERSE">&#x201C;Time: Blank Verse&#x201D;</span></a>, 
                        which <span class="persName-JR">Ruskin</span> prepared as a 
                        <a href="../notes/new_years_poems.php" target="_blank" class="inactive">New Year&#x02BC;s poem</a> for <span class="date-1827-01-01">1 January 1827</span>, 
                        and <a href="../apparatuses/spring_blank_verse_apparatus.php" target="_blank"><span class="title-poem-SPRINGBLANKVERSE">&#x201C;Spring: Blank Verse&#x201D;</span></a>, 
                        which <span class="persName-JR">Ruskin</span> composed in <span class="date-1827-05">May 1827</span>.
            </p>
            
            <p>Moreover, if <span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span> complements the drawing on the back endboard of 
                        <a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a>, 
                        <a href="../drawings/heights_of_wisdom_drawing_msi.jpg" target="_blank" class="inactive">&#x201C;Heights of Wisdom, Depth of Fools&#x201D;</a>
                        (see <a href="#DISCUSSION">Discussion</a>), then the poem&#x02BC;s date can be pinpointed at <span class="date-1827-03-21">21 March 1827</span>.
            </p>
            
            <p>See <a href="../notes/date_citation_note.php" target="_blank">System of Date Citation</a>.
            </p>
            
         </div>
         
         <div id="COMPPUB" class="section">
            <span class="head-section">Composition and Publication</span>
            
            <p>Excerpt quoted by <span class="persName-JR">Ruskin</span> in <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 35:57; also
                        quoted in <a href="../bibiliography/bibliography.php#POEMS4D1891" target="_blank" class="inactive"><i>Poems</i> (4<sup>o</sup>, 1891)</a>,
                        1:xxi; <a href="../bibiliography/bibliography.php#POEMS8D1891" target="_blank" class="inactive"><i>Poems</i> (8<sup>o</sup>, 1891)</a>,
                        1:viii; and <a href="../bibiliography/bibliography.php#JRWORKSLE" target="_blank" class="inactive">Ruskin, <i>Works</i></a>, 2:254. Previously unpublished in entirety.
            </p>
            
            <p>Hand is pencil, print; see <a href="../notes/hand_note.php" target="_blank">Ruskin&#x02BC;s Handwriting</a>.
            </p>
            
            <p>No details of composition are known; however, the poem&#x02BC;s possible connections with drawings in <a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a> 
                        suggest a motive for composition (see <a href="#DISCUSSION">Discussion</a>)
            </p>
            
         </div>
         
         <div id="DISCUSSION" class="section">
            <span class="head-section">Discussion</span>
            
            <p><span class="persName-JR">Ruskin</span>&#x02BC;s pun in the repeated word <i>reflect</i>, meaning both <i>contemplate</i> and 
                        <i>mirror</i>, becomes evident when the poem&#x02BC;s source is identified not only with <span class="title-scripture-GENESIS">Genesis</span> 9:16, 
                        but also in a scientific work, such as <span class="persName-JJ">Jeremiah Joyce</span>&#x02BC;s 
                        <span class="title-lesson-JOYCESCIENTIFICDIALOGUES-italic">Scientific Dialogues</span>, 
                        in which the rainbow is explained as a phenomenon of light
                        reflection and refraction (<a href="../bibiliography/bibliography.html#JOYCEDIALOGUES" target="_blank" class="inactive">Joyce, <i>Dialogues</i></a>, conversation 18, &#x201C;Of the Rainbow&#x201D; &#x005B;3:94&#x2013;100&#x005D;).
            </p>
            
            <p>The poem is possibly linked to two drawings in <a href="../apparatuses/msi_apparatus.php" target="_blank"><span class="title-manuscript-MSI">MS I</span></a>. 
                        In the <a href="../drawings/frontispiece_msi_drawing.jpg" target="_blank" class="inactive">&#x201C;Frontispiece&#x201D;</a> or plate &#x201C;1&#x201D; 
                        to <span class="title-lesson-HARRYANDLUCYVOL1">&#x201C;Harry and Lucy,&#x201D; Vol. 1</span>, <span class="persName-JR">Ruskin</span> 
                        crudely depicts a rainbow in a rainy sky, from which the sun has emerged 
                        (the bow resembles the outline of a hill, but <span class="persName-JR">Ruskin</span> helpfully labels the object as a &#x201C;rainbow&#x201D;). 
                        Since the frontispiece takes up an entire page by itself and faces the title page of the volume, no evidence precludes <span class="persName-JR">Ruskin</span> 
                        having drawn this picture at the same time that he entered <span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span> at the end of the notebook. 
                        It is just as likely, however, that <span class="persName-JR">Ruskin</span> added this poem in <span class="date-1827">1827</span> to the end of 
                        <a href="../apparatuses/msi_poetry_anthology_apparatus.php" target="_blank"><span class="title-anthology-MSIPOETRYANTHOLOGY">&#x201C;Poetry&#x201D; &#x005B;MS I Poetry Anthology&#x005D;</span></a> in order to 
                        complement the pre&#x2010;existing frontispiece, which he would have drawn in <span class="date-1826-09-01-1826-10-31">September&#x2013;October 1826</span> 
                        to serve as both the frontispiece and a part&#x2010;title page for <span class="title-lesson-HARRYANDLUCYVOL1">&#x201C;Harry and Lucy,&#x201D; Vol. 1</span>. 
                        (That the drawing belongs to the earlier period is suggested by its level of skill, exhibiting <span class="persName-JR">Ruskin</span>&#x02BC;s most juvenile tendency 
                        to line up objects on a single plane, a feature that is gradually replaced by more complex perspectives over the sequence of this <a href="../notes/red_book_note.php" target="_blank">Red Book</a>&#x02BC;s 
                        illustrations (see <a href="../apparatuses/harry_and_lucy_vol1_apparatus.php#DISCUSSION" target="_blank" class="inactive"><span class="title-lesson-HARRYANDLUCYVOL1">&#x201C;Harry and Lucy,&#x201D; Vol. 1</span>: Discussion</a>).
            </p>
            
            <p>A second drawing, <a href="../drawings/heights_of_wisdom_drawing_msi.jpg" target="_blank" class="inactive">&#x201C;Heights of Wisdom, Depth of Fools&#x201D;</a>,
                        which concludes the volume on the back endboard, represents an allegorical diagram of the &#x201C;heights of wisdom&#x201D;
                        and &#x201C;depths of folly.&#x201D; Wisdom and folly might describe those who, according to this poem, reflect or
                        fail to reflect, respectively, on the sign of God&#x02BC;s covenant. This drawing is dated <span class="date-1827-03-21">21 March</span>, which 
                        (provided that date refers to <span class="date-1827">1827</span>, and not <span class="date-1826">1826</span>) would accord
                        with a tentative dating of composition to <span class="date-1827-01-01-1827-03-31">January&#x2013;March 1827</span>. 
                        The allegorical drawing and <span class="title-poem-ONTHERAINBOW">&#x201C;On the Rainbow: In Blank Verse&#x201D;</span> also converge with 
                        <a href="../apparatuses/look_at_that_ship_apparatus.php" target="_blank"><span class="title-poem-THESHIP1827">&#x201C;The Ship&#x201D; <i>and</i> &#x201C;Look at That Ship&#x201D; &#x005B;1827&#x005D;</span></a>, 
                        which <span class="persName-JR">Ruskin</span> dated as &#x201C;<span class="date-1827-02-1827-03"><span class="sic">febuary &#x005B;sic&#x005D;</span> or march 1827</span>.&#x201D;
            </p>
            
